unsigned char * readYUV(char *path)  
{  
  
    FILE *fp;  
    unsigned char * buffer;  
    long size = 1280 * 720 * 3 / 2;  
  
    if((fp=fopen(path,"rb"))==NULL)  
    {  
       printf("cant open the file");  
       exit(0);  
    }  
  
    buffer = new unsigned char[size];  
    memset(buffer,'\0',size);  
    fread(buffer,size,1,fp);  
    fclose(fp);  
    return buffer;  
} 

